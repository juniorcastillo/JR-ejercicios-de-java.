/**
* Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle do-while .
* junior castillo
*/

public class Ejercicio3Bucle{
  public static void main(String[] args) {
    
    
       int j=0;
       do {System.out.println(j);
        j+=5;
      }
      while (j < 100);
        
  }
}

    
