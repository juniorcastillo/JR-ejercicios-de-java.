/**
* Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle
for .
* @junior castillo
*/
public class Ejercicio5Bucle {
  public static void main(String[] args) {
      
      int j=320;
       
      while (j >= 160){
      
        System.out.println(j);
         j -= 20;
        
      }
  }
}
    
