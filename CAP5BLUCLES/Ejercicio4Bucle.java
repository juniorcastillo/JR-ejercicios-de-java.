/**
* Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle
for .
* @junior castillo
*/
public class Ejercicio4Bucle {
  public static void main(String[] args) {
      
       
      for ( int j=320; j >= 160; j -= 20) {
        System.out.println(j);
        
      }
  }
}
    
